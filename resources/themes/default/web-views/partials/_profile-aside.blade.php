<script>
    $(document).on('click', '#asideDeleteAccountBtn', function(e) {
        e.preventDefault();

        Swal.fire({
            title: '{{ translate("are_you_sure") }}?',
            text: '{{ translate("want_to_delete_this_account") }}?',
            icon: 'warning',
            showCancelButton: true,
            confirmButtonColor: '#d33',
            cancelButtonColor: '#3085d6',
            confirmButtonText: '{{ translate("yes_delete_it") }}',
            cancelButtonText: '{{ translate("cancel") }}'
        }).then((result) => {
            if (result.isConfirmed) {
                $.ajax({
                    url: '{{ route("account-delete", auth("customer")->id()) }}',
                    type: 'POST',
                    data: {
                        _token: '{{ csrf_token() }}',
                        _method: 'DELETE'
                    },
                    success: function(response) {
                        Swal.fire({
                            icon: 'success',
                            title: '{{ translate("deleted") }}',
                            text: response.message || '{{ translate("your_account_has_been_deleted") }}',
                            timer: 2000,
                            showConfirmButton: false
                        }).then(() => {
                            window.location.href = '{{ route("home") }}';
                        });
                    },
                    error: function() {
                        toastr.error('{{ translate("unable_to_delete_account") }}');
                    }
                });
            }
        });
    });
</script>